var title = document.querySelector(".panel1");
var opa = "";
var height = "";
var visible = "";
var padding = "";

title.addEventListener("click", () => {
	var titleEl = title
		.parentNode
		.querySelector(".content1")
		.style;

	if(titleEl.opacity == "1") {
		opa = "0";
		height = "0px";
		visible = "hidden";
		padding = "0px 10px 0px 10px";
	} else {
		height = "auto";
		visible = "visible";
		opa = "1";
		padding = "10px";
	}
	
		
		titleEl.opacity = opa
		titleEl.height = height;
		titleEl.visibility = visible;
		titleEl.padding = padding;
})








